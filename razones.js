(function initStarfield() {
  const canvas = document.getElementById('starfield');
  const ctx    = canvas.getContext('2d');
  let stars    = [];
  let W, H;

  function resize() {
    W = canvas.width  = window.innerWidth;
    H = canvas.height = window.innerHeight;
    stars = [];
    for (let i = 0; i < 150; i++) {
      stars.push({
        x: Math.random() * W,
        y: Math.random() * H,
        r: Math.random() * 1.4 + 0.3,
        opacity: Math.random() * 0.6 + 0.1,
        speed:   Math.random() * 0.12 + 0.04,
        twinkle: Math.random() * Math.PI * 2,
        twinkleSpeed: (Math.random() * 0.02 + 0.005),
      });
    }
  }

  function draw() {
    ctx.clearRect(0, 0, W, H);
    stars.forEach(function(s) {
      s.twinkle += s.twinkleSpeed;
      s.y -= s.speed;
      if (s.y < -2) { s.y = H + 2; s.x = Math.random() * W; }
      const op = s.opacity * (0.5 + 0.5 * Math.sin(s.twinkle));
      ctx.beginPath();
      ctx.arc(s.x, s.y, s.r, 0, Math.PI * 2);
      ctx.fillStyle = 'rgba(255,245,220,' + op.toFixed(3) + ')';
      ctx.fill();
    });
    requestAnimationFrame(draw);
  }

  resize();
  window.addEventListener('resize', resize);
  draw();
})();

// ── FX Particles ─────────────────────────────────
const FX = (function() {
  const canvas  = document.getElementById('fx-canvas');
  const ctx     = canvas.getContext('2d');
  let W, H;
  let particles = [];
  let running   = false;

  function resize() {
    W = canvas.width  = window.innerWidth;
    H = canvas.height = window.innerHeight;
  }
  resize();
  window.addEventListener('resize', resize);

  function Particle(x, y, type) {
    this.x    = x; this.y = y;
    this.vx   = (Math.random() - 0.5) * 6;
    this.vy   = (Math.random() * -6) - 2;
    this.life = 1;
    this.decay = 0.012 + Math.random() * 0.015;
    this.type  = type || 'rect';
    this.color = type === 'heart' ? '#e87070' :
                 type === 'star'  ? '#e7c15f' :
                 ['#c77d5a','#e7c15f','#c9a227','#ece2d6','#b7e0c0'][Math.floor(Math.random()*5)];
    this.size  = 6 + Math.random() * 8;
    this.rotation    = Math.random() * Math.PI * 2;
    this.rotSpeed    = (Math.random() - 0.5) * 0.2;
    this.gravity     = 0.18;
  }

  function loop() {
    ctx.clearRect(0, 0, W, H);
    particles.forEach(function(p) {
      p.x  += p.vx;
      p.y  += p.vy;
      p.vy += p.gravity;
      p.life -= p.decay;
      p.rotation += p.rotSpeed;
      if (p.life <= 0) return;
      ctx.save();
      ctx.globalAlpha = Math.max(0, p.life);
      ctx.translate(p.x, p.y);
      ctx.rotate(p.rotation);
      ctx.fillStyle = p.color;
      if (p.type === 'heart') {
        ctx.font = p.size + 'px serif';
        ctx.fillText('♥', -p.size/2, p.size/2);
      } else if (p.type === 'star') {
        ctx.font = p.size + 'px serif';
        ctx.fillText('★', -p.size/2, p.size/2);
      } else {
        ctx.fillRect(-p.size/2, -p.size/3, p.size, p.size * 0.6);
      }
      ctx.restore();
    });
    particles = particles.filter(function(p) { return p.life > 0; });
    if (particles.length === 0) running = false;
    else requestAnimationFrame(loop);
  }

  function trigger(x, y, count, type) {
    count = count || 30;
    for (let i = 0; i < count; i++) {
      particles.push(new Particle(x, y, type));
    }
    if (!running) { running = true; loop(); }
  }

  function epicMode() {
    let t = 0;
    const interval = setInterval(function() {
      for (let i = 0; i < 8; i++) {
        trigger(Math.random() * W, Math.random() * H * 0.6, 1, t % 2 === 0 ? 'heart' : 'star');
      }
      t++;
      if (t > 60) clearInterval(interval);
    }, 50);
  }

  return { trigger: trigger, epicMode: epicMode };
})();

